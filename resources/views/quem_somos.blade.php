        <h2>O que fazemos?</h2>
        <br>
        <p> Uma iniciativa destinada a abrigar e fortalecer projetos inovadores e novas empresas de base tecnológica, por um período de tempo limitado.
            A IETEC-CEFET/RJ - Incubadora de Empresas Tecnológicas do CEFET/RJ - possui um ambiente físico que se destina a abrigar negócios e projetos que necessitem de um desenvolvimento tecnológico, de formação empreendedora e de estruturação gerencial.
            Constitui, assim, um meio inovador que gera condições para aumentar as chances de sobrevivência, crescimento e consolidação de microempresas inovadoras.
            Idealizada há mais de 20 anos, vem consolidando sua estratégia de expansão aos campi de Nova Iguaçu, Maria da Graça, Petrópolis, Valença, Angra dos Reis, Itaguaí e Nova Friburgo.
            Entregou ao mercado 24 empreendimentos cujo faturamento médio de 6,5 milhões e mais de 600 postos de trabalho especializados criados demonstra a força e importância das incubadoras para o ecossistema de inovação.
            Além disso, a IETEC CEFET/RJ assumiu a tarefa de provocar na comunidade acadêmica as discursões sobre o ensino do empreendedorismo em parceria com o SEBRAE RJ através dos simpósios anuais de Educação Empreendedora.
            Outra ação em andamento e em conformidade com a missão da incubadora e o pensamento das Cidades Inteligentes, Humanas e Sustentáveis apregoado pela Anprotec é a estruturação do L.U.E. - Laboratório de Uso Específico - que atenderá a comunidade de especialistas e inventores no desenvolvimento de protótipos robustos para demonstração às empresas e governos que demonstrem interesse por soluções criativas e inovadoras.
            Assim, a IETEC perssegue o interesse público que busca soluções inovadoras para os seus processos, produtos e serviços oferecendo um espaço de diálogo e de trocas de experiências.</p>
